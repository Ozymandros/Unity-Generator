class TestRenderTemplate:
    def test_render_automated_setup(self) -> None:
        templates_dir = Path(__file__).resolve().parents[1] / "templates" / "unity"
        if not templates_dir.exists():
            pytest.skip("Templates directory not found")

        rendered = render_template(
            "AutomatedSetup.cs.j2",
            {
                "install_packages": True,
                "packages": ["com.unity.textmeshpro"],
                "generate_scene": True,
                "setup_urp": False,
                "scene_name": "TestScene",
            },
            templates_dir,
        )

        assert "ProjectInitializer" in rendered
        assert "PackageInstaller" in rendered
        assert "SceneGenerator" in rendered
        assert "TestScene" in rendered
        # URP should not be included
        assert "ProjectSettingsConfigurator" not in rendered

    def test_render_minimal_setup(self) -> None:
        templates_dir = Path(__file__).resolve().parents[1] / "templates" / "unity"
        if not templates_dir.exists():
            pytest.skip("Templates directory not found")

        rendered = render_template(
            "AutomatedSetup.cs.j2",
            {
                "install_packages": False,
                "packages": [],
                "generate_scene": False,
                "setup_urp": False,
                "scene_name": "MainScene",
            },
            templates_dir,
        )

        assert "ProjectInitializer" in rendered
        assert "ImportValidator" in rendered
        # Optional sections should not be present
        assert "PackageInstaller" not in rendered
        assert "SceneGenerator" not in rendered


