class TestZipProject:
    def test_creates_zip(self, tmp_path: Path) -> None:
        project = tmp_path / "MyProject"
        project.mkdir()
        (project / "Assets").mkdir()
        (project / "Assets" / "test.cs").write_text("code", encoding="utf-8")

        zip_path = zip_project(project)

        assert zip_path.exists()
        assert zip_path.suffix == ".zip"

    def test_custom_output_path(self, tmp_path: Path) -> None:
        project = tmp_path / "MyProject"
        project.mkdir()
        (project / "file.txt").write_text("data", encoding="utf-8")
        custom_zip = tmp_path / "custom.zip"

        result = zip_project(project, custom_zip)

        assert result == custom_zip
        assert custom_zip.exists()


