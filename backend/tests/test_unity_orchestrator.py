class TestParseEditorLog:
    def test_empty_log(self) -> None:
        result = parse_editor_log("")
        assert result["errors"] == []
        assert result["warnings"] == []

    def test_detects_cs_errors(self) -> None:
        log = "Loading...\nAssets/Scripts/Foo.cs(10,5): error CS1002: ; expected\nDone.\n"
        result = parse_editor_log(log)
        assert len(result["errors"]) == 1
        assert "CS1002" in result["errors"][0]

    def test_detects_cs_warnings(self) -> None:
        log = "Assets/Scripts/Bar.cs(3,1): warning CS0168: Variable declared but never used\n"
        result = parse_editor_log(log)
        assert len(result["warnings"]) == 1
        assert "CS0168" in result["warnings"][0]

    def test_detects_compilation_failed(self) -> None:
        log = "Compilation failed: some detail\n"
        result = parse_editor_log(log)
        assert len(result["errors"]) == 1

    def test_detects_fatal_error(self) -> None:
        log = "Fatal Error!\n"
        result = parse_editor_log(log)
        assert len(result["errors"]) == 1

    def test_detects_abort_batchmode(self) -> None:
        log = "Aborting batchmode due to failure\n"
        result = parse_editor_log(log)
        assert len(result["errors"]) == 1

    def test_mixed_content(self) -> None:
        log = (
            "Normal log line\n"
            "Assets/X.cs(1,2): error CS0001: bad\n"
            "Assets/Y.cs(3,4): warning CS0168: unused\n"
            "Another normal line\n"
        )
        result = parse_editor_log(log)
        assert len(result["errors"]) == 1
        assert len(result["warnings"]) == 1


