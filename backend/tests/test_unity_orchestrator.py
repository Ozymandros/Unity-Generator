class TestScriptInjection:
    def test_inject_creates_files(self, tmp_path: Path) -> None:
        scripts = {
            "Setup.cs": "using UnityEditor; // setup",
            "Helper.cs": "using UnityEngine; // helper",
        }
        inject_dir = inject_editor_scripts(tmp_path, scripts)

        assert inject_dir.exists()
        assert (inject_dir / "Setup.cs").exists()
        assert (inject_dir / "Helper.cs").exists()
        assert (inject_dir / "Setup.cs").read_text(encoding="utf-8") == scripts["Setup.cs"]

    def test_inject_directory_structure(self, tmp_path: Path) -> None:
        inject_dir = inject_editor_scripts(tmp_path, {"Test.cs": "content"})
        assert inject_dir == tmp_path / "Assets" / "Editor" / "AutoGenerated"

    def test_cleanup_removes_directory(self, tmp_path: Path) -> None:
        inject_dir = inject_editor_scripts(tmp_path, {"Test.cs": "content"})
        assert inject_dir.exists()
        cleanup_injected_scripts(inject_dir)
        assert not inject_dir.exists()

    def test_cleanup_nonexistent_is_safe(self, tmp_path: Path) -> None:
        # Should not raise
        cleanup_injected_scripts(tmp_path / "nonexistent")


